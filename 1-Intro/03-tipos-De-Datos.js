/*Tipos de datos en JS.
Entero, Flotante, Cadena, Null, Undefine*/

//los datos primitivos son datos simple o que contiene un unico valor,
// por otro lado los datos no primiitivos son asociados a datos o variable complejas como los objetos.

let miEntero = 10;
console.log(miEntero);

let miFlotante = 7.9;
console.log(miFlotante);

let miCadena = ("Hola");
console.log(miCadena);

let miBoolean = true;
console.log(miBoolean);

let miNull = null;
console.log (miNull);

let miUndefined = undefined;
console.log(miUndefined);

//conocer el tipo de dato de una variable.
console.log(typeof miCadena);
console.log(typeof miBoolean);

